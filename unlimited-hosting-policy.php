<div class="doc-h2title">Unlimited Hosting Space; excessive SQL Database</div>
<p class="doc-p1">Mera13Host does not set any specific limits on the amount of disk space a hosting subscriber can use for their website, nor does Mera13Host charge additional fees based on an increased amount of storage used, provided the hosting subscriber's use of storage complies with these Terms. Please note, however, that the Mera13Host service is designed to host websites. Mera13Host does NOT provide unlimited hosting for online storage, videos, images, audio, games, installers, executable files, backups, or archiving of electronic files, documents, log files, web spider or indexer, irc, torrent, file sharing, peer-to-peer activities, testing environment etc., and any such prohibited use of the Services will result in the termination of hosting subscriber's account, with or without notice. Accounts with an excessive database size (i.e., more than 1 GB) negatively affect the performance of the server. Mera13Host may request that the database size be reduced to ensure proper performance or may disable the Subscriber's account, with or without notice. Hosting subscriber's can create multiple databases however size of each database should not be more than 1 GB.</p>
