<div class="doc-h2title">Unlimited File Transfer</div>
<p class="doc-p1">Mera13Host does not set any limits on the amount of visitor traffic a website can receive or on the amount of content a hosting subscriber can upload to his/her/its website in a given month, nor does Mera13Host charge additional fees based on increased use of bandwidth, as long as the hosting subscriber's use of the Services complies with these Terms. In most cases, a hosting subscriber's website will be able to support as much traffic as the hosting subscriber can legitimately acquire. However, Mera13Host reserves the right to limit processor time, bandwidth, processes, or memory in cases where it is necessary to prevent negatively impacting other hosting subscribers.</p>
